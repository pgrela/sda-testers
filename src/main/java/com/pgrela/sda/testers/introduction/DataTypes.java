package com.pgrela.sda.testers.introduction;

public class DataTypes {
    public static void main(String[] args) {
        byte aByte = 3;
        System.out.println("Value of aByte is: " + aByte);
        short aShort = 3;
        System.out.println("Value of aShort is: " + aShort);
        int aInt = 3;
        System.out.println("Value of aInt is: " + aInt);
        long aLong = 1_000_000_000_000_000L;
        System.out.println("Value of aLong is: " + aLong);
        float aFloat = 3.14f;
        System.out.println("Value of aFloat is: " + aFloat);
        double aDouble = 314;
        System.out.println("Value of aDouble is: " + aDouble);
        char aChar = 'x';
        System.out.println("Value of aChar is: " + aChar);
        boolean aBoolean = true;
        System.out.println("Value of aBoolean is: " + aBoolean);
    }
}
